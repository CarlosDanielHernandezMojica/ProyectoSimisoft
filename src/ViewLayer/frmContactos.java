    private void bModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarActionPerformed
        if (jtContactos.getSelectedRow() >= 0) {
            int idProducto = (int) jtContactos.getValueAt(jtContactos.getSelectedRow(), 0);
            frmNContacto obj = new frmNContacto(idProducto);
            obj.setTitle("Modificar Contacto");
            obj.setModal(true);
            obj.setVisible(true);
            jtContactos.setModel(new Contactos().GetAllModelNew());

        } else {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar un producto");
        }
    }//GEN-LAST:event_bModificarActionPerformed


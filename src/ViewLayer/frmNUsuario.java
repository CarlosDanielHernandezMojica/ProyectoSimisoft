    private void bGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGuardarActionPerformed
        int idSucursal = 0;
        for (int i = 0; i < sucursal.GetAllModel().getRowCount(); i++) {
            if (cbSucursal.getSelectedItem().equals(sucursal.GetAllModel().getValueAt(i, 1))) {
                idSucursal = (int) sucursal.GetAllModel().getValueAt(i, 0);
                System.out.println(sucursal.GetAllModel().getValueAt(i, 0));
            }
        }
        int idContacto = 0;
        for (int i = 0; i < contacto.GetAllModel().getRowCount(); i++) {
            if (cbContactos.getSelectedItem().equals(contacto.GetAllModel().getValueAt(i, 1))) {
                idContacto = (int) contacto.GetAllModel().getValueAt(i, 0);
                System.out.println(contacto.GetAllModel().getValueAt(i, 0));
            }
        }
        
        if (usuario.getIdUsuario()> 0) {
            usuario.setNombre(tNombre.getText());
            usuario.setTipoUsuario(tTipo.getText());
            usuario.setUsuario(tUsuario.getText());
            usuario.setPassword(tPassword.getText());
            usuario.setIdSucursal(idSucursal);
            usuario.setIdContacto(idContacto);
            usuario.setActivo(true);

            if (usuario.Update()) {
                JOptionPane.showMessageDialog(null, "Producto actualizado correctamente");
                this.dispose();
            }

        } else {
            if (new Usuarios(0, tNombre.getText(),
                tTipo.getText(),
                tUsuario.getText(),
                tPassword.getText(),
                idSucursal,
                idContacto,
                true).Add()) {
            JOptionPane.showMessageDialog(this, "Producto agregado correctamente");
            this.dispose();
        }
        }
    }//GEN-LAST:event_bGuardarActionPerformed

